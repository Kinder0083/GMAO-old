    # ==============================================
    # Feature 1: Generate Checklist from Document
    # ==============================================
    def test_generate_checklist_endpoint_exists(self):
        """POST /api/ai-maintenance/generate-checklist should not return 404"""
        # Call without file to check endpoint exists (should return 422 not 404)
        response = requests.post(
            f"{BASE_URL}/api/ai-maintenance/generate-checklist",
            headers=self.headers_multipart
        )
        # 422 = validation error (no file), not 404 (route not found)
        assert response.status_code != 404, f"Endpoint not found! Got: {response.status_code}"
        print(f"✓ Endpoint exists, returned status {response.status_code} (expected 422 without file)")
        
    def test_generate_checklist_requires_file(self):
        """Generate checklist should require a file upload"""
        response = requests.post(
            f"{BASE_URL}/api/ai-maintenance/generate-checklist",
            headers=self.headers_multipart
        )
        # Should return 422 (Unprocessable Entity) because file is required
        assert response.status_code == 422, f"Expected 422, got {response.status_code}"
        print(f"✓ Correctly returns 422 when no file provided")

