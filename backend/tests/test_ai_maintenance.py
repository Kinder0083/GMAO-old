    def test_analyze_nonconformities_returns_result(self):
        """Analyze non-conformities should return a result (even without historical data)"""
        response = requests.post(
            f"{BASE_URL}/api/ai-maintenance/analyze-nonconformities",
            headers=self.headers,
            json={"days": 90}
        )
        
        assert response.status_code == 200, f"Expected 200, got {response.status_code}: {response.text}"
        data = response.json()
        assert data.get("success") == True, f"Expected success=True, got {data}"
        assert "data" in data, "Response should contain 'data' field"
        
        # Even without data, it should return a valid structure
        analysis = data.get("data", {})
        assert "summary" in analysis, "Analysis should contain 'summary'"
        
        print(f"✓ Analysis returned successfully: {analysis.get('summary', 'No summary')[:100]}...")

    def test_analyze_nonconformities_with_different_periods(self):
        """Analyze non-conformities with different period parameters"""
        for days in [30, 90, 180]:
            response = requests.post(
                f"{BASE_URL}/api/ai-maintenance/analyze-nonconformities",
                headers=self.headers,
                json={"days": days}
            )
            
            assert response.status_code == 200, f"Failed for {days} days: {response.status_code}"
            data = response.json()
            assert data.get("success") == True
            
            print(f"✓ Analysis with {days} days period: OK")

    # ==============================================
    # Authentication Tests
    # ==============================================
    def test_endpoints_require_authentication(self):
        """All AI maintenance endpoints should require authentication"""
        endpoints = [
            ("POST", "/api/ai-maintenance/generate-checklist"),
            ("POST", "/api/ai-maintenance/create-checklists-batch"),
            ("POST", "/api/ai-maintenance/generate-maintenance-program"),
            ("POST", "/api/ai-maintenance/create-maintenance-batch"),
            ("POST", "/api/ai-maintenance/analyze-nonconformities")
        ]
        
        for method, endpoint in endpoints:
            if method == "POST":
                response = requests.post(f"{BASE_URL}{endpoint}")
            
            # Should return 401 or 403 without token
            assert response.status_code in [401, 403], \
                f"{endpoint} should require auth, got {response.status_code}"
            print(f"✓ {endpoint} requires authentication")


